package Day_27;

public class Wrapper_class {
    public static void main(String[] args) {

        //Integer

        int x = 12;

        Integer objectSayi = new Integer(12);
        System.out.println("objectSayi:" + objectSayi);

        //CHARACTER

        char a = 'a';
        Character objectChar = new Character(a);
        System.out.println("objectChar:" + objectChar);

        //Double

        Double objectDouble = new Double(15.7);
        System.out.println("objectDouble:" + objectDouble);























    }
}
